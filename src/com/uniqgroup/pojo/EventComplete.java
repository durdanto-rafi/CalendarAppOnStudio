package com.uniqgroup.pojo;

public class EventComplete {
	int _id;
	int _event_id;
	String _day;
	String _week;
	
	
	public EventComplete(){
		
	}


	public int get_id() {
		return _id;
	}


	public void set_id(int _id) {
		this._id = _id;
	}


	public int get_event_id() {
		return _event_id;
	}


	public void set_event_id(int _event_id) {
		this._event_id = _event_id;
	}


	public String get_day() {
		return _day;
	}


	public void set_day(String _day) {
		this._day = _day;
	}


	public String get_week() {
		return _week;
	}


	public void set_week(String _week) {
		this._week = _week;
	}
}
